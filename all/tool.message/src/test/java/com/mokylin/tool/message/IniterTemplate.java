package com.mokylin.tool.message;

public class IniterTemplate {
	private String name;
	private String id;
	private String msgName;
	private String handlerName;
	private String shortHandlerName;
	public String getMsgName() {
		return msgName;
	}
	public void setMsgName(String msgName) {
		this.msgName = msgName;
	}
	public String getHandlerName() {
		return handlerName;
	}
	public void setHandlerName(String handlerName) {
		this.handlerName = handlerName;
	}
	public String getShortHandlerName() {
		return shortHandlerName;
	}
	public void setShortHandlerName(String shortHandlerName) {
		this.shortHandlerName = shortHandlerName;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
}

